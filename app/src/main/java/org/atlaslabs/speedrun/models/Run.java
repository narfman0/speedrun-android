package org.atlaslabs.speedrun.models;

public class Run {
    String user, game, category, platform, time;

    public String getUser(){
        return user;
    }

    public String getGame() {
        return game;
    }

    public String getCategory() {
        return category;
    }

    public String getPlatform() {
        return platform;
    }

    public String getTime() {
        return time;
    }
}
